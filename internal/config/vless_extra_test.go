// ─── validateVLESSParams: граничные значения портов ───────────────────────

func TestValidateVLESSParams_PortBoundary(t *testing.T) {
	base := &VLESSParams{
		Address: "h.com", UUID: "u", SNI: "s", PublicKey: "k", ShortID: "i",
	}

	cases := []struct {
		port    int
		wantErr bool
	}{
		{0, true},
		{-1, true},
		{65536, true},
		{100000, true},
		{1, false},
		{80, false},
		{443, false},
		{65535, false},
	}
	for _, tc := range cases {
		p := *base
		p.Port = tc.port
		err := validateVLESSParams(&p)
		if (err != nil) != tc.wantErr {
			t.Errorf("validateVLESSParams(port=%d) error=%v, wantErr=%v", tc.port, err, tc.wantErr)
		}
	}
}
