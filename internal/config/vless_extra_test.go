// ─── parseVLESSKey: кэширование ───────────────────────────────────────────

func TestParseVLESSKey_Caching_SameResult(t *testing.T) {
	path := filepath.Join(t.TempDir(), "cached.key")
	url := "vless://uuid@host.com:443?sni=x.com&pbk=k&sid=s"
	os.WriteFile(path, []byte(url), 0644)

	p1, err := parseVLESSKey(path)
	if err != nil {
		t.Fatalf("first parse: %v", err)
	}
	p2, err := parseVLESSKey(path)
	if err != nil {
		t.Fatalf("second parse: %v", err)
	}
	// Кэш должен вернуть тот же объект (тот же адрес)
	if p1.Address != p2.Address || p1.Port != p2.Port {
		t.Errorf("кэшированный результат отличается: %v vs %v", p1, p2)
	}
}

